int[,] RandomRecursion(int rows, int columns)
{
    int[,] recursion = new int[rows, columns];
    for (int i = 0; i < recursion.GetLength(0); i++)
    {
        for (int j = 0; j < recursion.GetLength(1); j++)
        {
            recursion[i, j] = new Random().Next(1, 10);
        }
    }
    return recursion;
}

void Print(int[,] matrix)
{
    for (int rows = 0; rows < matrix.GetLength(0); rows++) // GetLenhts(0) - означает обращение к первому значению в скобках [3]
    {
        for (int columns = 0; columns < matrix.GetLength(1); columns++) // GetLenghts(1) - обращение ко 2 элементу в скобках [4]
            System.Console.Write($"{matrix[rows, columns]} ");
        System.Console.WriteLine();
    }
}

